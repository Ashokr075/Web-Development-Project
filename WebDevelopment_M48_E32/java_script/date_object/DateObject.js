// date object
console.log(Date());
let dateObject=new Date();
// getDate():dateCountNumber
console.log(dateObject.getDate());
// getMonth():monthCountNumber
console.log(dateObject.getMonth());
// getFullYear():fullYear
console.log(dateObject.getFullYear());
// getDay():dayCountNumber
console.log(dateObject.getDay());
// getHours():hours
console.log(dateObject.getHours());
// getMinutes():minutes
console.log(dateObject.getMinutes());
// getSeconds():seconds
console.log(dateObject.getSeconds());
// getMilliseconds():milliSeconds
console.log(dateObject.getMilliseconds());
// toLocaleDateString():fullDate(month/date/year)
console.log(dateObject.toLocaleDateString());
// toLocaleTimeString():hours:minutes:seconds 
console.log(dateObject.toLocaleTimeString());