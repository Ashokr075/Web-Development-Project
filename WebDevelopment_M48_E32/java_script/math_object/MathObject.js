// math object
let mathObject=Math;
// PI:piValue
console.log(mathObject.PI);
// pow(baseValue,powerValue):poweredValue
console.log(mathObject.pow(10,2));
// sqrt(number):squareRootNumber
console.log(mathObject.sqrt(81));
// cbrt(number):cubeRootNumber
console.log(mathObject.cbrt(8));
// max(elements):maximumElement
console.log(mathObject.max(87,54,35,69,45,-10,13)); 
// min(elements):minimumElement
console.log(mathObject.min(87,54,35,69,45,-10,13)); 
// round(number):roundedNumber
console.log(mathObject.round(3.25));
// trunc(number):integer
console.log(mathObject.trunc(3.25));
// ceil(number):topNearestInteger
console.log(mathObject.ceil(7.05));
// floor(number):bottomNearestInteger
console.log(mathObject.floor(7.95));
// random():randomNumber(between 0 to 1)
console.log(mathObject.trunc(mathObject.random()*1000000));